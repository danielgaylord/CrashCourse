name = 'Dan'

print('Is name == "Dan"? I think it is!')
print(name == "Dan")

print('Is name != "Dan"? No, no, no!')
print(name != "Dan")

print('Is name == "dan"? Close, but no cigar!')
print(name == "dan")

print('Is name.lower() == "dan"? Yeah!')
print(name.lower() == "dan")

print('Is name.upper() == "DAN"? You betcha!')
print(name.upper() == "DAN")

print('Is name == "Joe"? Are you crazy?!')
print(name == "Joe")

print('Is name.capitalize() == "DAN"? Nah!')
print(name.capitalize() == "DAN")

print('Is name < "Zach"? It should be!')
print(name < "Zach")

print('Is name > "Adam"? Why, yes!')
print(name > "Adam")

print('Is name.swapcase() == "Dan"? Not a chance!')
print(name.swapcase() == "Dan")

